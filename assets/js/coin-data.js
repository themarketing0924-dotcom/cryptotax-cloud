// ── 빠른 검색 유틸 ──────────────────────────────────────────
function searchCoins(query, limit) {
  if (!query) return COIN_DB.slice(0, limit || 20);
  var q = query.toUpperCase().trim();
  var results = COIN_DB.filter(function(c) {
    return c.s.indexOf(q) !== -1 ||
           c.n.indexOf(query) !== -1 ||
           c.en.toUpperCase().indexOf(q) !== -1 ||
           c.cat.indexOf(query) !== -1;
  });
  return results.slice(0, limit || 50);
}

